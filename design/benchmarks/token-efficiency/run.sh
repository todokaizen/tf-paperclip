#!/usr/bin/env bash
# Phase 1 token-efficiency benchmark runner.
#
# Creates a fresh issue assigned to the mock-website VentureLead, which
# triggers an assignment-wake heartbeat server-side. Waits for that run
# to finish, then reads its usageJson from /heartbeat-runs/<id>. Writes
# a JSON result under design/benchmarks/token-efficiency/results/.
#
# We do NOT fire our own `paperclipai heartbeat run` — it would land as
# a dedup-skip *after* the assignment-wake and pollute the measurement.
# The assignment-wake IS the benchmarked unit.
#
# Preconditions (checked, not auto-fulfilled — fail loud with hints):
#   * paperclipai server running (default http://localhost:3100)
#   * mock-website company imported
#     (pnpm paperclipai company import ./companies/mock-website --new-company-name "Mock Website")
#   * MOCK_VL_AGENT_ID env var set to the venture-lead's agent UUID
#     (GET /api/companies/<id>/agents)
#   * MOCK_COMPANY_ID env var set to the mock-website company UUID
#   * MOCK_PROJECT_ID env var set to the demo project UUID
#     (GET /api/companies/<id>/projects)
#   * PAPERCLIP_API_BASE (optional; default http://localhost:3100)
#   * PAPERCLIP_API_KEY (or local_trusted header mode — see below)
#
# Env overrides for variants (Phase 2):
#   BENCH_VARIANT       label in the results filename (default: baseline)
#   BENCH_TIMEOUT_SECS  max wait for the run to complete (default: 300)
#
# Exit codes:
#   0 success
#   1 precondition failure
#   2 no assignment-wake run appeared within the timeout
#   3 the heartbeat run ended in a non-success status
set -euo pipefail

here="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
repo_root="$(cd "$here/../../.." && pwd)"
results_dir="$here/results"
mkdir -p "$results_dir"

: "${MOCK_VL_AGENT_ID:?set MOCK_VL_AGENT_ID to the venture-lead agent UUID (GET /api/companies/<id>/agents)}"
: "${MOCK_COMPANY_ID:?set MOCK_COMPANY_ID to the mock-website company UUID (GET /api/companies)}"
: "${MOCK_PROJECT_ID:?set MOCK_PROJECT_ID to the demo project UUID (GET /api/companies/<id>/projects)}"
api_base="${PAPERCLIP_API_BASE:-http://localhost:3100}"
variant="${BENCH_VARIANT:-baseline}"
timeout_secs="${BENCH_TIMEOUT_SECS:-300}"

if [[ -n "${PAPERCLIP_API_KEY:-}" ]]; then
  auth=(-H "Authorization: Bearer ${PAPERCLIP_API_KEY}")
else
  auth=(-H "X-Local-Agent-Id: ${MOCK_VL_AGENT_ID}")
fi

if ! curl -fsS --max-time 5 "${auth[@]}" "$api_base/api/health" >/dev/null 2>&1; then
  echo "❌ Paperclip server not reachable at $api_base — start it with 'pnpm paperclipai run'" >&2
  exit 1
fi

if ! (cd "$repo_root" && pnpm -s paperclipai --help >/dev/null 2>&1); then
  echo "❌ 'pnpm paperclipai' not runnable from $repo_root — run 'pnpm install'" >&2
  exit 1
fi

# ---- stage fixture ---------------------------------------------------
fixture_src="$repo_root/companies/mock-website/fixture-repo/"
fixture_dst="/tmp/mock-website-current"
rm -rf "$fixture_dst"
mkdir -p "$fixture_dst"
rsync -a --delete "$fixture_src" "$fixture_dst/"

ts="$(date -u +%Y%m%dT%H%M%SZ)"
sha="$(cd "$repo_root" && git rev-parse --short HEAD)"
run_started_at="$(date -u +%Y-%m-%dT%H:%M:%S.000Z)"
base="$results_dir/${ts}-${sha}-${variant}"
log_file="${base}.log"
json_file="${base}.json"

# ---- sweep prior bench issues so the VL's inbox is clean -----------
# Each prior [bench:...] issue sits in the inbox as blocked and adds
# per-run overhead (VL iterates through all of them on every wake).
# Mark them done before the new run so the measurement doesn't drift.
prior_ids="$(curl -fsS --max-time 10 "${auth[@]}" \
  "$api_base/api/companies/$MOCK_COMPANY_ID/issues" 2>/dev/null \
  | jq -r '.[] | select(.title | startswith("[bench:")) | select(.status != "done" and .status != "cancelled") | .id' \
  2>/dev/null || true)"
swept=0
if [[ -n "$prior_ids" ]]; then
  while IFS= read -r prior_id; do
    [[ -z "$prior_id" ]] && continue
    (cd "$repo_root" && pnpm -s paperclipai issue update "$prior_id" --status done --json \
      >/dev/null 2>&1 || true)
    swept=$((swept + 1))
  done <<<"$prior_ids"
fi
[[ "$swept" -gt 0 ]] && echo "🧹 swept $swept prior bench issue(s) to done"

# ---- seed issue (this is what triggers the measured heartbeat) ------
issue_title="[bench:${variant}:${ts}] Fix Submit button colour (#888 → #007BFF)"
issue_desc="Change the .submit-button background from #888 to #007BFF in styles.css and update tests/button.test.js. See fixture-repo/README.md for details. This issue is generated by the token-efficiency benchmark at run ${ts}; it is not real work."
issue_json="$(cd "$repo_root" && pnpm -s paperclipai issue create \
  --company-id "$MOCK_COMPANY_ID" \
  --title "$issue_title" \
  --description "$issue_desc" \
  --assignee-agent-id "$MOCK_VL_AGENT_ID" \
  --project-id "$MOCK_PROJECT_ID" \
  --status todo \
  --json 2>&1 || true)"
issue_id="$(echo "$issue_json" | jq -r '.id // empty' 2>/dev/null || true)"
if [[ -z "$issue_id" ]]; then
  echo "❌ issue create failed — response:" >&2
  echo "$issue_json" >&2
  exit 1
fi
echo "🎫 created issue $issue_id ($issue_title)"

# ---- discover the assignment-triggered run --------------------------
# Poll active heartbeat runs scoped to our agent until one appears or
# timeout. Paperclip's list endpoint only returns queued/running runs —
# we want to catch it before it finishes.
echo "⏳ waiting for assignment-triggered heartbeat to appear..."
run_id=""
deadline=$((SECONDS + 60))
while (( SECONDS < deadline )); do
  listing="$(curl -fsS --max-time 5 "${auth[@]}" \
    "$api_base/api/companies/$MOCK_COMPANY_ID/heartbeat-runs" 2>/dev/null || echo '[]')"
  run_id="$(echo "$listing" | jq -r \
    --arg agent "$MOCK_VL_AGENT_ID" \
    '[.[] | select(.agentId == $agent)] | sort_by(.createdAt) | last | .id // empty' \
    2>/dev/null || true)"
  [[ -n "$run_id" ]] && break
  sleep 1
done

if [[ -z "$run_id" ]]; then
  # Edge case: run completed faster than our poll. Try to find it via
  # the issue's recent activity — the run id leaves traces in issue events.
  # Fallback: fail loud.
  echo "❌ no assignment-triggered heartbeat appeared for agent $MOCK_VL_AGENT_ID within 60s" >&2
  echo "   (issue $issue_id was created but may not have kicked a wake — check agent heartbeat.enabled)" >&2
  exit 2
fi
echo "🔍 watching heartbeat run $run_id"

# ---- poll until terminal --------------------------------------------
deadline=$((SECONDS + timeout_secs))
run_json=""
while (( SECONDS < deadline )); do
  run_json="$(curl -fsS --max-time 10 "${auth[@]}" \
    "$api_base/api/heartbeat-runs/$run_id" 2>/dev/null || echo 'null')"
  status="$(echo "$run_json" | jq -r '.status // empty' 2>/dev/null || true)"
  case "$status" in
    succeeded|failed|cancelled|timed_out)
      break
      ;;
  esac
  sleep 2
done

run_finished_at="$(date -u +%Y-%m-%dT%H:%M:%S.000Z)"
status="$(echo "$run_json" | jq -r '.status // "unknown"')"
usage="$(echo "$run_json" | jq '.usageJson // {}')"

# Snapshot the server-side log for debugging. Unwrap the NDJSON chunks
# into a plain text file so it's greppable (agent thinking / tool_use /
# tool_result lines all live inside .content -> JSON-stringified chunks).
log_envelope="$(curl -sS --max-time 15 "${auth[@]}" \
  "$api_base/api/heartbeat-runs/$run_id/log" 2>/dev/null || echo '{}')"
echo "$log_envelope" \
  | jq -r '.content // ""' \
  | jq -r -s 'split("\n") | map(select(length > 0) | fromjson | .chunk) | join("")' \
  > "$log_file" 2>/dev/null || echo "$log_envelope" > "$log_file"

# ---- fixture diff ---------------------------------------------------
fixture_diff="$(diff -ruN "$fixture_src" "$fixture_dst/" 2>/dev/null | head -200 || true)"

# ---- assemble result ------------------------------------------------
jq -n \
  --arg timestamp "$ts" \
  --arg commitSha "$sha" \
  --arg variant "$variant" \
  --arg agentId "$MOCK_VL_AGENT_ID" \
  --arg companyId "$MOCK_COMPANY_ID" \
  --arg projectId "$MOCK_PROJECT_ID" \
  --arg issueId "$issue_id" \
  --arg heartbeatRunId "$run_id" \
  --arg status "$status" \
  --arg startedAt "$run_started_at" \
  --arg finishedAt "$run_finished_at" \
  --argjson usage "$usage" \
  --arg fixtureDiff "$fixture_diff" \
  --arg logFile "$(basename "$log_file")" \
  '{
     timestamp: $timestamp,
     commitSha: $commitSha,
     variant: $variant,
     agentId: $agentId,
     companyId: $companyId,
     projectId: $projectId,
     issueId: $issueId,
     heartbeatRunId: $heartbeatRunId,
     status: $status,
     runStartedAt: $startedAt,
     runFinishedAt: $finishedAt,
     usage: $usage,
     fixtureDiffPreview: $fixtureDiff,
     logFile: $logFile
   }' > "$json_file"

# ---- summary --------------------------------------------------------
cost_usd="$(echo "$usage" | jq -r '.costUsd // 0')"
input_t="$(echo "$usage" | jq -r '.inputTokens // 0')"
cached_t="$(echo "$usage" | jq -r '.cachedInputTokens // 0')"
output_t="$(echo "$usage" | jq -r '.outputTokens // 0')"
model="$(echo "$usage" | jq -r '.model // "?"')"
biller="$(echo "$usage" | jq -r '.biller // "?"')"

echo ""
echo "── benchmark result ────────────────────────────────────────────"
echo "  variant:        $variant"
echo "  sha:            $sha"
echo "  issue:          $issue_id"
echo "  heartbeat run:  $run_id"
echo "  status:         $status"
echo "  model / biller: $model / $biller"
echo "  tokens:         input=$input_t  cached=$cached_t  output=$output_t"
echo "  cost (USD):     \$$cost_usd"
echo "  window:         $run_started_at → $run_finished_at"
echo "  log:            $log_file"
echo "  result:         $json_file"
echo "────────────────────────────────────────────────────────────────"

case "$status" in
  succeeded) exit 0 ;;
  *) exit 3 ;;
esac
